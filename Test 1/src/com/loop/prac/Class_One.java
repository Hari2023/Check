package com.loop.prac;

public class Class_One {
	
	public static void main(String[] args) {
		
		System.out.println("Increment");
		for (int i = 1; i<=5; i++) {
			System.out.println(i);
					
		}
		
		System.out.println("Decrement");
		for(int i =5; i>=1; i--) {
			System.out.println(i);
		}
		
		
	}

}
